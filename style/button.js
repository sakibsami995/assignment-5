
document.getElementById("btn-one").addEventListener('click', function(event) {
    event.preventDefault();
    alert("Board uploded Successfully")
    ; 
    const clickNumElement = document.getElementById("click-num");
    const convertedNum = parseInt(clickNumElement.innerText);
    const newNum = convertedNum + 1;
    clickNumElement.innerText = newNum;
    console.log(newNum);
    const clickNumTask = document.getElementById("task-num");
    const convertedNum2 = parseInt(clickNumTask.innerText);
    const newNum2 = convertedNum2 - 1;
    clickNumTask.innerText = newNum2;
    event.target.disabled = true;
});
